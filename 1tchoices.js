function leiaMaisCardUm(){
    let pontosUm=document.getElementById("pontosUm");
    let maisTextoUm=document.getElementById("maisUm");
    let btnLeiaMaisUm=document.getElementById("btnLeiaMaisUm");

    if(pontosUm.style.display === "none"){
        pontosUm.style.display="inline";
        maisTextoUm.style.display="none";
        btnLeiaMaisUm.innerHTML="Leia Mais"
    }else{
        pontosUm.style.display="none";
        maisTextoUm.style.display="inline";
        btnLeiaMaisUm.innerHTML="Leia Menos";
    }
}
